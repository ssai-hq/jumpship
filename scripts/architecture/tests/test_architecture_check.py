    def test_graph_and_report_are_deterministic(self) -> None:
        self.write("internal/contracts/doc.go", "package contracts\n")
        self.write(
            "internal/workspaces/doc.go",
            "package workspaces\nimport _ \"example.invalid/jumpship/internal/contracts\"\n",
        )

        first = json.dumps(check(self.root), sort_keys=True)
        second = json.dumps(check(self.root), sort_keys=True)

        self.assertEqual(first, second)
        self.assertEqual("pass", json.loads(first)["status"])

    @unittest.skipIf(not hasattr(os, "symlink"), "platform does not support symlinks")
    def test_output_writers_refuse_symlink_and_preserve_victim(self) -> None:
        for flag, name in (
            ("--graph-output", "graph"),
            ("--report-output", "report"),
        ):
            with self.subTest(flag=flag):
                victim = self.write(f"{name}-victim.json", '{"authority":"preserve"}\n')
                output = self.root / f"evidence/{name}.json"
                output.parent.mkdir(parents=True, exist_ok=True)
                output.symlink_to(victim)
                errors = io.StringIO()

                with contextlib.redirect_stderr(errors):
                    result = architecture_main(
                        ["--root", str(self.root), flag, f"evidence/{name}.json"]
                    )

                self.assertEqual(1, result)
                self.assertEqual('{"authority":"preserve"}\n', victim.read_text(encoding="utf-8"))
                self.assertTrue(output.is_symlink())
                self.assertIn("non-regular output", errors.getvalue())

    @unittest.skipIf(not hasattr(os, "symlink"), "platform does not support symlinks")
    def test_output_writer_refuses_symlinked_parent(self) -> None:
        alias = self.root / "aliased-evidence"
        alias.mkdir()
        (self.root / "evidence").symlink_to(alias, target_is_directory=True)
        errors = io.StringIO()

        with contextlib.redirect_stderr(errors):
            result = architecture_main(
                ["--root", str(self.root), "--report-output", "evidence/report.json"]
            )

        self.assertEqual(1, result)
        self.assertFalse((alias / "report.json").exists())
        self.assertIn("not a real directory", errors.getvalue())

    def test_output_writer_regenerates_existing_regular_file(self) -> None:
        output = self.write("evidence/report.json", "stale\n")

        result = architecture_main(
            ["--root", str(self.root), "--report-output", "evidence/report.json"]
        )

        self.assertEqual(0, result)
