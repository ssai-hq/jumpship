    def test_tsconfig_resolution_fields_cannot_escape_web(self) -> None:
        cases = {
            "baseUrl": (
                '{"compilerOptions":{"baseUrl":"../internal"}}\n',
                "baseUrl escapes",
            ),
            "paths": (
                '{"compilerOptions":{"baseUrl":".","paths":{"@domain/*":["../internal/*"]}}}\n',
                "path alias",
            ),
            "include": (
                '{\n// JSONC is intentional\n"include":["../internal/**/*",],\n}\n',
                "include entry escapes",
            ),
            "reference": (
                '{"references":[{"path":"../internal"}]}\n',
                "project reference escapes",
            ),
            "rootDirs": (
                '{"compilerOptions":{"rootDirs":["src","../internal"]}}\n',
                "rootDirs entry escapes",
            ),
            "typeRoots": (
                '{"compilerOptions":{"typeRoots":["src/types","../internal/types"]}}\n',
                "typeRoots entry escapes",
            ),
            "plugins": (
                '{"compilerOptions":{"plugins":[{"name":"module-resolver"}]}}\n',
                "plugins require",
            ),
        }
        for field, (configuration, expected) in cases.items():
            with self.subTest(field=field):
                self.write("web/tsconfig.json", configuration)

                violations = check(self.root)["forbidden_edge_report"]["violations"]

                self.assertEqual(1, len(violations))
                self.assertEqual("web-configuration-boundary", violations[0]["rule"])
                self.assertIn(expected, violations[0]["explanation"])

    def test_relative_extends_recursively_validates_non_tsconfig_json(self) -> None:
        self.write(
            "web/config/base.json",
            '{"compilerOptions":{"paths":{"@domain/*":["../../internal/*"]}}}\n',
        )
        self.write("web/tsconfig.json", '{"extends":"./config/base.json"}\n')

        violations = check(self.root)["forbidden_edge_report"]["violations"]

        self.assertEqual(1, len(violations))
        self.assertEqual("web/config/base.json", violations[0]["source_file"])
        self.assertIn("path alias", violations[0]["explanation"])

    def test_relative_extends_cycle_fails_closed(self) -> None:
        self.write("web/config/a.json", '{"extends":"./b.json"}\n')
        self.write("web/config/b.json", '{"extends":"./a.json"}\n')
        self.write("web/tsconfig.json", '{"extends":"./config/a.json"}\n')

        violations = check(self.root)["forbidden_edge_report"]["violations"]

        self.assertEqual(1, len(violations))
        self.assertIn("cycle", violations[0]["explanation"])

    def test_jsconfig_and_unverified_bundler_aliases_are_checked(self) -> None:
        self.write(
            "web/jsconfig.json",
            '{"compilerOptions":{"baseUrl":".","paths":{"@domain/*":["../internal/*"]}}}\n',
        )
