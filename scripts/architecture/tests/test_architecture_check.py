#!/usr/bin/env python3
"""Adversarial tests for the static architecture checker."""

from __future__ import annotations

import contextlib
import io
import json
import os
import sys
import tempfile
import unittest
from pathlib import Path

sys.dont_write_bytecode = True
sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

from architecture_check import check, main as architecture_main


class ArchitectureCheckTests(unittest.TestCase):
    def setUp(self) -> None:
        self.temporary = tempfile.TemporaryDirectory()
        self.root = Path(self.temporary.name)
        self.write("go.mod", "module example.invalid/jumpship\n\ngo 1.25\n")

    def tearDown(self) -> None:
        self.temporary.cleanup()

    def write(self, relative: str, content: str = "") -> Path:
        path = self.root / relative
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content, encoding="utf-8")
        return path

    def violations(self) -> list[dict[str, object]]:
        return check(self.root)["forbidden_edge_report"]["violations"]

    def test_scans_generated_build_tagged_and_test_sources(self) -> None:
        self.write("internal/harness/doc.go", "package harness\n")
        self.write(
            "internal/mothership/generated_never.go",
            "//go:build never\n\n// Code generated by fixture. DO NOT EDIT.\n"
            "package mothership\nimport _ \"example.invalid/jumpship/internal/harness\"\n",
        )
        self.write(
            "internal/mothership/mothership_test.go",
            "package mothership_test\nimport _ \"example.invalid/jumpship/internal/harness\"\n",
        )

        violations = self.violations()

        self.assertEqual(1, len(violations))
        self.assertEqual("mothership-narrow-authority", violations[0]["rule"])
        self.assertTrue(violations[0]["direct"])

    def test_rejects_transitively_laundered_import(self) -> None:
        self.write("internal/harness/doc.go", "package harness\n")
        self.write(
            "internal/workspaces/workspaces.go",
            "package workspaces\nimport _ \"example.invalid/jumpship/internal/harness\"\n",
        )
        self.write(
            "cmd/mothership/main.go",
            "package main\nimport _ \"example.invalid/jumpship/internal/workspaces\"\n",
        )

        violation = self.violations()[0]

        self.assertFalse(violation["direct"])
        self.assertEqual(
            ["cmd/mothership", "internal/workspaces", "internal/harness"],
            violation["path"],
        )

    def test_nested_module_cannot_hide_forbidden_package(self) -> None:
        self.write("internal/harness/go.mod", "module example.invalid/harness\n\ngo 1.25\n")
        self.write("internal/harness/doc.go", "package harness\n")
        self.write(
            "cmd/mothership/main.go",
            "package main\nimport _ \"example.invalid/harness\"\n",
        )

        report = check(self.root)

        self.assertEqual(2, len(report["import_graph"]["modules"]))
        self.assertEqual(1, report["forbidden_edge_report"]["count"])

    def test_nested_module_cannot_disguise_guarded_external_provider(self) -> None:
        provider = "github.com/aws/aws-sdk-go-v2/service/bedrockruntime"
        self.write("internal/adapter/go.mod", f"module {provider}\n\ngo 1.25\n")
        self.write("internal/adapter/doc.go", "package adapter\n")
        self.write(
            "internal/engine/engine.go",
            f'package engine\nimport _ "{provider}"\n',
        )

        report = check(self.root)
        violations = report["forbidden_edge_report"]["violations"]

        self.assertEqual(1, len(violations))
        self.assertEqual(f"external:{provider}", violations[0]["target"])

    @unittest.skipIf(not hasattr(os, "symlink"), "platform does not support symlinks")
    def test_rejects_source_symlink_and_repository_escape(self) -> None:
        outside = Path(self.temporary.name).parent / f"{self.root.name}-outside.go"
        outside.write_text("package outside\n", encoding="utf-8")
        try:
            (self.root / "internal").mkdir(exist_ok=True)
            (self.root / "internal" / "escape.go").symlink_to(outside)

            report = check(self.root)

            self.assertEqual("fail", report["status"])
            self.assertEqual("symlink-escape", report["symlink_report"]["violations"][0]["kind"])
        finally:
            outside.unlink(missing_ok=True)

